public class WSSecurity {

	private String _Username;

	public void setUsername(String username) {
		_Username = username;
	}

	public String getUsername() {
		return _Username;
	}
	
	
	public void setPassword(String password) {
		_Password = password;
	}

	public String getPassword() {
		return _Password;
	}


	private String _Password;
	
}
